	public function send()
	{
		header('Content-Type: application/json');
		if(!$this->input->post('email') || !$this->input->post('message')){
			die(json_encode(array('success'=>false,'message'=>'Invalid request')));
		}
		if(!$this->validateEmail($this->input->post('email'))){
			die(json_encode(array('success'=>false,'message'=>'Invalid email address')));
		}
		$this->load->model('Request');
		if($this->Request->save_request($this->input->post('email'), $this->input->post('message'))){
			echo json_encode(array('success'=>true));
		}
		else{
			echo json_encode(array('success'=>false,'message'=>'Couldn\'t process the request. Please try again.'));
		}
	}

